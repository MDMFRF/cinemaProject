    //add movie, seat and date
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String movieName = jTextField_addedMovie.getText();
        String seat = jComboBox_seat.getSelectedItem().toString();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dateFormat.format(jDateChooser_date.getDate());
        
        PreparedStatement st ;
        ResultSet rs;
        
        String status = "unbooked";
            
            
        try {
            String movieQuery = "INSERT INTO `movie`(`movie`, `Date/Time`, `Seat`, `Status`) VALUES (?,?,?,?)";
            st = MyConnection.getConnection().prepareStatement(movieQuery);
            st.setString(1, movieName);
            st.setString(2, date);
            st.setString(3, seat);
            st.setString(4, status);
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Movie added");
                
        } catch (SQLException ex) {
            Logger.getLogger(MovieEdit.class.getName()).log(Level.SEVERE, null, ex);
            }
            
             
  
    }//GEN-LAST:event_jButton1ActionPerformed

