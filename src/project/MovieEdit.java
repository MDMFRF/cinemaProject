    //view the added movie to the table
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        PreparedStatement st ;
        ResultSet rs;
            
        try {
            String Query = "SELECT * FROM movie ";
            st = MyConnection.getConnection().prepareStatement(Query);;
            rs = st.executeQuery();
            
            DefaultTableModel tm = (DefaultTableModel)table1.getModel();
            tm.setRowCount(0);
            
            while (rs.next()){
                
                tm.addRow(new String[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
            }  
        } catch (SQLException ex) {
            Logger.getLogger(MovieEdit.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_jButton2ActionPerformed

